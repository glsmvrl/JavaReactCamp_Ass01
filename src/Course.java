public class Course {
    String courseID;
    String courseName;
    String courseInstructor;
    String courseBook;

    public Course(String courseID, String courseName, String courseInstructor, String courseBook){
        this.courseID = courseID;
        this.courseName = courseName;
        this.courseInstructor = courseInstructor;
        this.courseBook = courseBook;
    }
}
